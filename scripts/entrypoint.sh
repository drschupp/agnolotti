#!/bin/bash

############################################################################
# Container Entrypoint
############################################################################

if [[ "$PRINT_ENV_ON_LOAD" = true || "$PRINT_ENV_ON_LOAD" = True ]]; then
    echo "=================================================="
    printenv
    echo "=================================================="
fi

if [[ "$WAIT_FOR_DB" = true || "$WAIT_FOR_DB" = True ]]; then
    dockerize \
        -wait tcp://$DB_HOST:$DB_PORT \
        -timeout 300s
fi

############################################################################
# Start App
############################################################################

exec "$@"
